func main() {
	// Load .env file for local development (silently ignored in production)
	_ = godotenv.Load()

	// Load configuration from environment
	cfg := config.Load()

	// Initialize structured logger
	logger := initLogger(cfg.LogLevel)
	defer logger.Sync()

	logger.Info("starting Clever AI Gate",
		zap.String("mode", cfg.GinMode),
		zap.Int("port", cfg.Port),
	)

	ctx := context.Background()

	// --- Step 1: Initialize database connection pool ---
	dbPool, err := database.NewPool(ctx, cfg.DatabaseURL, logger)
	if err != nil {
		logger.Fatal("failed to connect to database", zap.Error(err))
	}
	defer dbPool.Close()

	// --- Step 2: Run database migrations ---
	if err := database.RunMigrations(ctx, dbPool, logger); err != nil {
		logger.Fatal("failed to run migrations", zap.Error(err))
	}

	// --- Step 3: Initialize encryption vault ---
	vault, err := credentials.NewVault(cfg.MasterEncryptionKey)
	if err != nil {
		logger.Fatal("failed to initialize encryption vault", zap.Error(err))
	}

	// --- Step 4: Initialize Ristretto cache ---
	cacheStore, err := cache.New(cfg, logger)
	if err != nil {
		logger.Fatal("failed to initialize cache", zap.Error(err))
	}
	defer cacheStore.Close()

	// --- Step 5: Load routing pools from DB → cache ---
	syncManager := credentials.NewSyncManager(dbPool, cacheStore, vault, logger)
	if err := syncManager.LoadInitialState(ctx); err != nil {
		logger.Fatal("failed to load initial routing state", zap.Error(err))
	}

	// --- Step 6: Start LISTEN/NOTIFY watcher ---
	syncManager.StartListener()
	defer syncManager.Stop()

	// --- Step 7: Start telemetry pipeline ---
	telemetryPipeline := telemetry.NewPipeline(
		dbPool, logger,
		cfg.TelemetryQueueSize,
		cfg.TelemetryBatchSize,
		cfg.TelemetryFlushInterval,
	)
	telemetryPipeline.Start()
	defer telemetryPipeline.Stop()

	// --- Step 8: Build HTTP transport and proxy handler ---
	transport := proxy.BuildOptimizedTransport(cfg)
	httpClient := proxy.BuildHTTPClient(transport)
	proxyHandler := proxy.NewHandler(httpClient, cacheStore, logger)

	// --- Step 9: Initialize health handler ---
	healthHandler := health.New(dbPool)

	// --- Step 10: Build Gin engine with all routes ---
	engine := router.NewEngine(&router.Dependencies{
		Config: cfg,
		DB:     dbPool,
		Cache:  cacheStore,
		Vault:  vault,
		Logger: logger,
		Health: healthHandler,
		Proxy:  proxyHandler,
	})

	// --- Step 11: Start HTTP server ---
	srv := &http.Server{
		Addr:              fmt.Sprintf("0.0.0.0:%d", cfg.Port),
		Handler:           engine,
		ReadHeaderTimeout: 10 * time.Second,
		WriteTimeout:      0, // Disabled for streaming — context handles timeouts
		IdleTimeout:       120 * time.Second,
		MaxHeaderBytes:    1 << 20, // 1MB max header size
	}

	// Mark as ready
	healthHandler.SetReady(true)

	// Start server in goroutine
	go func() {
		logger.Info("server listening", zap.String("addr", srv.Addr))
		if err := srv.ListenAndServe(); err != nil && err != http.ErrServerClosed {
			logger.Fatal("server failed", zap.Error(err))
		}
	}()

	// --- Step 12: Graceful shutdown ---
	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)
	sig := <-quit

	logger.Info("shutdown signal received", zap.String("signal", sig.String()))

	// Mark as not ready (health check will fail, load balancer stops sending traffic)
	healthHandler.SetReady(false)

	// Give in-flight requests time to complete
	shutdownCtx, shutdownCancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer shutdownCancel()

	if err := srv.Shutdown(shutdownCtx); err != nil {
		logger.Error("server forced to shutdown", zap.Error(err))
	}

	logger.Info("server stopped gracefully")
}

// initLogger creates a production-tuned zap logger.
func initLogger(level string) *zap.Logger {
	var zapLevel zapcore.Level
	switch level {
	case "debug":
		zapLevel = zapcore.DebugLevel
	case "warn":
		zapLevel = zapcore.WarnLevel
	case "error":
		zapLevel = zapcore.ErrorLevel
	default:
		zapLevel = zapcore.InfoLevel
	}

	config := zap.Config{
		Level:            zap.NewAtomicLevelAt(zapLevel),
		Development:      false,
		Encoding:         "json",
		EncoderConfig:    zap.NewProductionEncoderConfig(),
		OutputPaths:      []string{"stdout"},
		ErrorOutputPaths: []string{"stderr"},
	}

	logger, err := config.Build(zap.AddStacktrace(zapcore.ErrorLevel))
	if err != nil {
		panic("failed to initialize logger: " + err.Error())
	}

	return logger
}
